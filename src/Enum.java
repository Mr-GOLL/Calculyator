    public static int calculator(int a, int b, char sign1) {
        if (a <= 0 || a > 10 || b <= 0 || b > 10) throw new IllegalArgumentException("Число больше 10");
        int result = 0;
        switch (sign1) {
            case '+':
                result = a + b;
                break;
            case '-':
                result = a - b;
                break;
            case '*':
                result = a * b;
                break;
            case '/':

                try {
                    result = a / b;
                } catch (ArithmeticException | InputMismatchException e) {
                    System.out.println("У вас тут ошибочка вышла : " + e);
                    System.out.println("Допускаются только целые числа");

                    break;
                }
                break;
            default:
                throw new IllegalArgumentException("Не верный знак!");

        }
        return result;
    }

